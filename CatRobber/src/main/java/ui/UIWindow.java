package ui;

import javafx.scene.Node;

/**
 * Інтерфейс для UI-вікон, які можна показувати, приховувати та отримувати їхній кореневий вузол
 */
public interface UIWindow {

    /** Показує вікно користувацького інтерфейсу */
    void show();

    /** Приховує вікно користувацького інтерфейсу */
    void hide();

    /**
     * Повертає кореневий вузол
     * @return кореневий елемент інтерфейсу
     */
    Node getRoot();
}
